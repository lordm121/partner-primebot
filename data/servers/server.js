const mongoose = require("mongoose");
module.exports = mongoose.model("servers", 
	new mongoose.Schema({
		id: String,
		name: String,
                guildID: String,
                serverNAME: String,
                serverID: String,
                serverid:String,
                languages:{type: String, default:"english"},
		icon: String,
		ownerID: String,
                backURL:String,
                colors:String,
		longDesc: String,
		shortDesc: String,
                prefix:String,
                channelID:String,
                tags: Array,
		link: String,
                invitelink: String,
		bump: {type: Date, default: null},
		votes: {type: Number, default: 0},
		bumps: {type: Number, default: 0},
		analytics: Object,
		analytics_visitors: Number,
		analytics_joins: Number,
		country: Object,
		rates: Object
	})
);
